export type IntNumberFormatOptionStyle =
  /**
   * A decimal style format
   */
  | "decimal"
  /**
   * A percent style format.
   */
  | "percent"
  /**
   * A currency style format that uses the currency symbol defined by the number formatter locale.
   */
